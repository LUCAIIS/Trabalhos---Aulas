# Demonstração dos Operadores Aritméticos em Python
# Declaração de variáveis
a = 10
b = 5
# Operações aritméticas
soma = a + b # Adição: 10 + 5 = 15
diferenca = a - b # Subtração: 10 - 5 = 5 
produto = a * b # Multiplicação: 10 * 5 = 50
divisao = a / b # Divisão real: 10 / 5 = 2.0
resto = a % b # Módulo (resto da divisão): 10 % 5 = 0
potencia = a ** b # Exponenciação: 10 ** 5 = 100000
div_inteira = a // b # Divisão inteira: 10 // 5 = 2
# Exibição dos resultados
print(f"Adição: {a} + {b} = {soma}")
print(f"Subtração: {a} - {b} = {diferenca}")
print(f"Multiplicação: {a} * {b} = {produto}")
print(f"Divisão: {a} / {b} = {divisao}")
print(f"Módulo: {a} % {b} = {resto}")
print(f"Exponenciação: {a} ** {b} = {potencia}")
print(f"Divisão Inteira: {a} // {b} = {div_inteira}")