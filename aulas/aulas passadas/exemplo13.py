numero = int(input("Digite um numeo"))
if numero > 0:
    print(f"O valor ({numero}) está categorizado como um numero positivo")
elif numero < 0:
    print(f"O valor ({numero}) está categorizado como um numero negativo")
elif numero == 0:
    print(f"O valor ({numero}) corresponte ao numero 0, ava")
else:
    print("essa mensagem nao deveira estar sendo mostrada")
